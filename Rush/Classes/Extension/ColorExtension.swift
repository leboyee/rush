import Foundation
import UIKit

extension UIColor {

    class var bgBlack: UIColor {
        return UIColor(red: 0.11, green: 0.11, blue: 0.12, alpha: 1)
    }
    
    class var bgBlack17: UIColor {
        return UIColor(red: 0.17, green: 0.17, blue: 0.18, alpha: 1)
    }
    
    class var bgWhite96: UIColor {
        return UIColor(red: 0.96, green: 0.96, blue: 0.97, alpha: 1)
    }
    
    class var brown24: UIColor {
        return UIColor(red: 0.56, green: 0.24, blue: 0.33, alpha: 1)
    }
}
